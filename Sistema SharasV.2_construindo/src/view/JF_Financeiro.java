    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        int mes = jmes_financeiro.getMonth();
        mes = mes + 1;

        financeiroDao.consultarFinanceiro(mes, txt_financeiro_treinamento, txt_financeiro_serragem, txt_financeiro_racao, txt_financeiro_feno, txt_financeiro_veterinario, txt_financeiro_manutencao, txt_financeiro_ferrajamento, this);
        int treinamento = Integer.valueOf(txt_financeiro_treinamento.getText());
        int serragem = Integer.valueOf(txt_financeiro_serragem.getText());
        int racao = Integer.valueOf(txt_financeiro_racao.getText());
        int feno = Integer.valueOf(txt_financeiro_feno.getText());
        int veterinario = Integer.valueOf(txt_financeiro_veterinario.getText());
        int manutencao = Integer.valueOf(txt_financeiro_manutencao.getText());
        int ferrajamento = Integer.valueOf(txt_financeiro_ferrajamento.getText());

        int totald = serragem + racao + feno + veterinario + manutencao + ferrajamento;
        int total = treinamento - totald;
        txt_financeiro_total.setText("" + total);
    }//GEN-LAST:event_jButton22ActionPerformed

